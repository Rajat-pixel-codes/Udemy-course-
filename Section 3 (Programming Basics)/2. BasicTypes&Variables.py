# Integer and float:
print(5)  # 5
print(5+6)  # 11
print(2.3+5.66)  # 7.96

a = 5
print(a)  # 5

# String:
print('She said, "I was here!"')  # She said, "I was here!"
print("This cost " + str(6)+ " dollars")  # This cost 5 dollars
print("This cost " + str(6+5)+ " dollars")  # This cost 11 dollars
print("Hello:World".split(":"))  # [ Hello, World ]
print("My name is "+ "Muiz:Ahmed:Khan".split(":")[0])  # My name is Muiz

Str = "this is a string"
print(Str)  # this is a string

# Boolean:
print(5 == 5)  # True
print(5 == 4)  # False
print(5 is 5)  # True
print(5 is not 5)  # False
print("This" is "This")  # True
print("This" is "That")  # False
print("True" is True)  # False
print("True" is str(True))  # True

flag = True
print(flag == True)  # True

# Array
arr = [1,2,3,4]
print(arr[3])  # 4

